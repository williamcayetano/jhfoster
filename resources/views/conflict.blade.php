Section 1502 of the Dodd-Frank Wall Street Reform and Consumer Protection Act passed in July of 2010, is an attempt to prevent rebel groups in the Democratic Republic of the Congo (DRC) from illegally using profits from the minerals trade to fund their activities. The act requires publicly-traded corporations to report the use of “Conflict Minerals” in their products. “Conflict Minerals” refers to specific mineral ores and their derivatives (gold, tantalum, tin, and tungsten) which originate from the DRC or specified adjoining countries.
<br /><br />
<br /><br />
John Henry Foster, as a privately held corporation, is not subject to the Conflict Minerals reporting requirements; however, as a supplier to publicly-traded companies, we are committed to helping our customers comply with their reporting requirements. In addition, John Henry Foster is fully supportive of global industry efforts to ensure responsible manufacturing processes with transparent sourcing in full accordance with the law.
<br /><br />
<br /><br />
We are continuously surveying our suppliers to ascertain that there are no Conflict Minerals in the materials they supply to us. We have the expectation that all of our suppliers will be in compliance with Dodd-Frank regulations; non-compliance will trigger a review prior to continuation of the business relationship.
<br /><br />
<br /><br />
Currently we can represent that, based on the information available, our products do not contain Conflict Minerals. We will updated this statement as new information is received.
